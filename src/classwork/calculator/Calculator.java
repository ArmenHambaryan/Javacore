package classwork.calculator;

public class Calculator {

    double plus(double i, double x) {
        // double d= i+x;
        //return d
        return i + x;
    }
    double minus(double i, double x) {
        return i - x;
    }
    double multiple(double i, double x) {
        return i * x;
    }
    double divide(double i, double x) {
        return i / x;
    }
}
